package Module20_Java_lang;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.cbrt(8));
        System.out.println(Math.decrementExact(43));
        System.out.println(Math.getExponent(12.32));
        System.out.println(Math.floorDiv(30,9));
        System.out.println(Math.tan(45* (Math.PI/180)));
        System.out.println(Math.toDegrees(45));
        System.out.println(Math.toRadians(45));
    }
}
